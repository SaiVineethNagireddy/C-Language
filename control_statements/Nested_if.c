#include<stdio.h>
int main()
{
    int i=2;
    int j=3;
    //Nested loop
    if (i<j)
    {
        if (i==j)
        {
            printf("The value of i is equal to j");
        }
        else
        {
            printf("The value of i is not equal to j");
        }
        
    }
    return 0;
}