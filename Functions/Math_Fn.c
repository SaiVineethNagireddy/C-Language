#include<stdio.h>
#include<math.h>

int main()
{
    printf("%.2f \n",sqrt(25));//Square root
    printf("%.2f \n",ceil(2.5));//lowest to the point value
    printf("%.2f \n",floor(2.5));//Highest value nearest to the integer
    printf("%.2f",pow(5,3));//gives the value of a to the power of b
    return 0;
}